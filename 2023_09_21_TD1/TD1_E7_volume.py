# Calculer le volume d'un parallélépipède
print("=== Je vais calculer pour vous le volume d'un parallélépipède. ===")
print()

width = float(input("Veuillez saisir la largeur du parallélépipède (Appuyez sur 'Entrée' pour valider) : "))
length = float(input("Veuillez saisir la longueur du parallélépipède (Appuyez sur 'Entrée' pour valider) : "))
height = float(input("Veuillez saisir la hauteur du parallélépipède (Appuyez sur 'Entrée' pour valider) : "))

print()
print(f"Le volume d'un parrallélépipède avec une largeur de {width}, une longueur de {length} et une hauteur de {height} vaux {width*length*height}³.")