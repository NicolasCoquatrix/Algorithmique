# Déffinir si un coureur est dans la catégorie Benjamin

print("="*99)
print("-"*15, "Je vais déterminer si vous êtes rattaché à la catégorie 'Benjamin'.", "-"*15,"\n")

age = int(input("Veuillez saisir votre age et appuyez sur la touche 'Entrée' pour valider : "))
print()

if age == 12 or age == 13 :
    print("Vous êtes dans la catégorie 'Benjamin'.","\n")
else :
    print("Vous n'êtes pas dans la catégorie 'Benjamin'.","\n")

print("="*99,"\n")

